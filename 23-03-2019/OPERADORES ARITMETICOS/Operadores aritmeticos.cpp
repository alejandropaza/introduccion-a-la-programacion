#include<iostream>
using namespace std;

int main(){
	
	int resultado;
	

	
	//suma(+)
	resultado = 4 + 6;
	cout << "suma: " <<resultado<<endl;
	//resta(-)
	resultado = 4 - 6;
	cout << "resta: " <<resultado<<endl;
	//mult(*)
	resultado = 4 * 6;
	cout << "multiplicacion: " <<resultado<<endl;
	//div(/)
	resultado = 4 / 6;
	cout << "division: " <<resultado<<endl;
	//mod(%)
	resultado = 4 % 6;
	cout << "modulo: " <<resultado<<endl;
	
		return 0;
}
